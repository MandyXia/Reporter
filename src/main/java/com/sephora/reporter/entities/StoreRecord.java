package com.sephora.reporter.entities;

import javax.persistence.Entity;
import javax.persistence.Table;

@Entity
@Table(name = "StoreRecord")
public class StoreRecord extends BaseEntity {
	private int code;
	private String name;
	private String area;
	private int year;
	private int month;

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}
}
